#define fastio ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL)
 
using namespace std;
 
#define ll long long
#define endl "\n"
 
 int gcd(int a, int b, int& x, int& y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int x1, y1;
    int d = gcd(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return d;
}


int main()
{
    fastio;
    int a,b;
    while(cin>>a>>b){
        int x,y,g;
        g=gcd(a,b,x,y);
        cout<<x<<" "<<y<<" "<<g<<endl;
    }
    return 0;
}